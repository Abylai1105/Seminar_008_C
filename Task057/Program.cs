double lenght = 10;
for (double i = 0; i < lenght; i++)
{
    double result = ChekNumber(i);
    Console.WriteLine($" Введенное чисто {i} встречается {result} раз ");
}

double ChekNumber(double number)
{
    int count = 0;
    for (int i = 0; i < array.GetLength(0); i++)
    {
        for (int j = 0; j < array.GetLength(1); j++)
        {
            if (number == array[i, j])
            {
                count++;
            }
        }
    }
    return count;
}

void PrintArray(double[,] matrix)
{
    for (int rows = 0; rows < matrix.GetLength(0); rows++)
    {
        for (int columns = 0; columns < matrix.GetLength(0); columns++)
        {
            Console.Write($" {matrix[rows, columns]} ");
        }
        Console.WriteLine();
    }
}
void FillArray(double[,] matrix)
{
    for (int rows = 0; rows < matrix.GetLength(0); rows++)
    {
        for (int columns = 0; columns < matrix.GetLength(0); columns++)
        {
            matrix[rows, columns] = new Random().Next(1,10);
        }
        Console.WriteLine();
    }
}